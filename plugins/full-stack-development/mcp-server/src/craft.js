export function reviewRequirementWorkspaceStage(input = {}) {
  const targetStage = input.targetStage ?? "visual-design";
  const root = requirementWorkspaceRoot(input);
  const { map: fileMap, outside } = workspaceFileMap(input.files ?? input.artifacts ?? [], root);
  const requiredFiles =
    targetStage === "implementation" ? WORKSPACE_FILES_BEFORE_IMPLEMENTATION : WORKSPACE_FILES_BEFORE_VISUAL;
  const issues = [];
  const { stageState, issue: stageIssue } = parseWorkspaceStage(fileMap);

  if (outside.length > 0) {
    issues.push({
      severity: "blocker",
      code: "artifact-outside-fixed-workspace",
      message: `Requirement artifacts must live under ${root}. Found outside paths: ${outside.join(", ")}`
    });
  }

  for (const file of requiredFiles) {
    if (!fileMap.has(file)) {
      issues.push(workspaceMissingIssue(file));
    }
  }

  if (stageIssue) {
    issues.push(stageIssue);
  }

  if (fileMap.has(WORKSPACE_STAGE_FILE) && !requirementsApproved(stageState)) {
    issues.push({
      severity: "blocker",
      code: "requirements-not-approved",
      message:
        "The fixed requirement workspace exists, but 00-stage.json does not mark requirementsApproved=true or a visual-design-ready stage."
    });
  }

  if (targetStage === "implementation" && !stageVisualApproved(stageState, input.visualConfirmation)) {
    issues.push({
      severity: "blocker",
      code: "missing-approved-visual-confirmation",
      message:
        "Implementation is blocked until Pencil/Figma desktop, mobile, component-detail, and state-matrix images are exported and approved by the user."
    });
  }

  const currentStage = deriveRequirementWorkspaceStage(fileMap, input.visualConfirmation, stageState);

  return {
    status: issues.length ? "blocked" : "pass",
    fixedRoot: root,
    currentStage,
    targetStage,
    nextAllowedStage: issues.length ? currentStage : targetStage,
    requiredFiles: requiredFiles.map((file) => workspacePath(root, file)),
    issues,
    policy: [
      "Do not start Pencil/Figma unless this review passes for visual-design.",
      "Do not start front-end implementation unless this review passes for implementation.",
      "A loose requirements document outside the fixed workspace does not satisfy the gate.",
      "The stage file is the source of truth for current requirement progress and change handling."
    ]
  };
}

export function planRequirementChange(input = {}) {
  const featureName = input.featureName ?? "HeroUI Frontend";
  const featureSlug = slugify(input.featureSlug ?? featureName);
  const root = requirementWorkspaceRoot({
    outputRoot: input.outputRoot,
    featureSlug
  });
  const changeNumber = Number.isInteger(input.changeNumber) ? input.changeNumber : 1;
  const changeId = String(changeNumber).padStart(4, "0");
  const impactedLayers = detectImpactedLayers(input.changeRequest, input.impactedLayers);
  const earliest = impactedLayers[0];
  const impactedArtifacts = impactedLayers.map((layer) => layer.artifact);
  const invalidates = [];

  if (impactedLayers.some((layer) => layer.key !== "acceptance")) {
    invalidates.push("Pencil/Figma visual approval");
  }

  if (/implementation|verification|done|complete/i.test(String(input.currentStage ?? ""))) {
    invalidates.push("front-end implementation");
  }

  return {
    featureName,
    featureSlug,
    currentStage: input.currentStage ?? "unknown",
    changeRequest: input.changeRequest ?? "",
    changeFilePath: workspacePath(root, `changes/${changeId}-change-request.md`),
    resetToStage: earliest.stage,
    impactedLayers: impactedLayers.map((layer) => layer.key),
    impactedArtifacts,
    invalidates,
    stageUpdate: {
      file: workspacePath(root, WORKSPACE_STAGE_FILE),
      currentStage: earliest.stage,
      nextAllowedStage: "requirements",
      visualDesignApproved: false,
      implementationApproved: false
    },
    requiredActions: [
      "append the change record under changes/ before editing visuals or code",
      "update 00-stage.json with the regressed currentStage and invalidated approvals",
      `update impacted artifacts: ${impactedArtifacts.join(", ")}`,
      "re-run review_requirement_workspace_stage for visual-design before Pencil/Figma",
      "re-export and re-approve Pencil/Figma visuals if any visual, IA, interaction, state, product, or engineering artifact changed",
      "re-run review_requirement_workspace_stage for implementation before code changes"
    ]
  };
}

