function normalizePath(filePath) {
  return filePath.replaceAll("\\", "/");
}

function slugify(value = "front-end-surface") {
  const slug = String(value)
    .normalize("NFKD")
    .replace(/[^\w\s-]/g, "")
    .trim()
    .toLowerCase()
    .replace(/[\s_]+/g, "-")
    .replace(/-+/g, "-");

  return slug || "front-end-surface";
}

function requirementWorkspaceRoot(input = {}) {
  const outputRoot = normalizePath(input.outputRoot ?? REQUIREMENT_WORKSPACE_ROOT).replace(/\/$/, "");
  const featureSlug = slugify(input.featureSlug ?? input.featureName ?? "front-end-surface");

  return `${outputRoot}/${featureSlug}`;
}

function workspacePath(root, file) {
  return `${root}/${file}`;
}

function workspaceBasename(path) {
  return normalizePath(path).split("/").pop();
}

function isInsideWorkspace(filePath, root) {
  const normalized = normalizePath(filePath ?? "").replace(/^\.\//, "");
  return normalized === root || normalized.startsWith(`${root}/`);
}

function normalizeArtifacts(artifacts = []) {
  if (Array.isArray(artifacts)) {
    return artifacts.map((artifact) => ({
      file: artifact.file ?? artifact.path ?? artifact.name,
      path: artifact.path ?? artifact.file ?? artifact.name,
      content: String(artifact.content ?? "")
    }));
  }

  return Object.entries(artifacts).map(([file, content]) => ({
    file,
    path: file,
    content: String(content ?? "")
  }));
}

function workspaceFileMap(files = [], root) {
  const map = new Map();
  const outside = [];

  for (const artifact of normalizeArtifacts(files)) {
    const path = normalizePath(artifact.path ?? artifact.file ?? "");
    if (!path) {
      continue;
    }

    if (!isInsideWorkspace(path, root)) {
      outside.push(path);
      continue;
    }

    map.set(workspaceBasename(path), {
      path,
      content: artifact.content
    });
  }

  return { map, outside };
}

function artifactByFile(artifacts) {
  const map = new Map();
  for (const artifact of normalizeArtifacts(artifacts)) {
    if (artifact.file) {
      map.set(artifact.file, artifact);
    }
  }
  return map;
}

