export function planHeroUiSystem(input = {}) {
  const productType = input.productType ?? "application";
  const primaryWorkflow = input.primaryWorkflow ?? "complete the main workflow efficiently";
  const componentSelection = selectHeroUiComponents({
    needs: [
      "navigation",
      "filters",
      productType,
      primaryWorkflow,
      "data table",
      "detail drawer",
      "validated form",
      "toast feedback",
      "loading state"
    ]
  });
  const componentNames = componentSelection.components.map((component) => component.name);
  const visualGate = planVisualConfirmation({
    components: componentNames,
    targetSurfaces: input.targetSurfaces
  });

  return {
    intent: {
      productType,
      audience: input.audience ?? "product users",
      primaryWorkflow,
      constraints: input.constraints ?? []
    },
    officialContext: getOfficialContext(),
    requirementPipeline: planFrontendRequirementPipeline({
      idea: input.idea,
      productType,
      audience: input.audience,
      primaryWorkflow
    }),
    visualGate,
    direction:
      "Use HeroUI React v3 primitives and Tailwind v4 styles to build a dense, scannable product surface after component-level visual approval.",
    layout: [
      "Navigation and breadcrumbs",
      "Toolbar with filters and search",
      "Table or structured list",
      "Drawer or modal detail workflow",
      "Toast, alert, skeleton, and error states"
    ],
    components: componentSelection.components,
    heroUiTokens: HERO_UI_SEMANTIC_TOKENS,
    verification: generateFrontendVerificationChecklist({
      appType: productType,
      viewports: ["1440x900", "768x1024", "390x844"],
      interactions: ["keyboard navigation", "filtering", "open Drawer", "submit Form", "show Toast"]
    }).sections.flatMap((section) => section.items),
    antiPatterns: [
      "Do not implement front-end code before Pencil/Figma images have been exported and approved by the user.",
      "Do not import from local shadcn component paths such as @/components/ui/*.",
      "Do not use @nextui-org/react legacy imports.",
      "Do not add HeroUIProvider for React v3 unless the official docs change; use I18nProvider only for locale setup."
    ]
  };
}

export function reviewHeroUiQuality(input = {}) {
  const code = String(input.code ?? "");
  const css = String(input.css ?? "");
  const issues = [];
  const rawColors = [...code.matchAll(RAW_COLOR_PATTERN)].map((match) => match[0]);
  const paletteFamilies = detectPaletteFamilies(code);

  if (!visualConfirmationApproved(input.visualConfirmation)) {
    issues.push({
      severity: "blocker",
      code: "blocked-no-visual-approval",
      message:
        "No approved Pencil/Figma image export was provided. Produce component-detail images and get user approval before coding."
    });
  } else {
    const missingCategories = missingVisualExportCategories(input.visualConfirmation);
    if (missingCategories.length > 0) {
      issues.push({
        severity: "blocker",
        code: "missing-visual-export-categories",
        message: `Pencil/Figma approval is incomplete. Missing export categories: ${missingCategories.join(", ")}.`
      });
    }
  }

  if (input.heroUiGapJustification && !heroUiGapExceptionComplete(input.heroUiGapJustification)) {
    issues.push({
      severity: "blocker",
      code: "incomplete-heroui-gap-exception",
      message:
        "HeroUI gap exceptions must include missing component, official sources checked, reason, and narrow local scope."
    });
  }

  if (SHADCN_IMPORT_PATTERN.test(code)) {
    issues.push({
      severity: "blocker",
      code: "shadcn-import",
      message: "Local shadcn component imports found. Replace with official @heroui/react components."
    });
  }

  if (HANDWRITTEN_UI_PATTERN.test(code) && !input.heroUiGapJustification) {
    issues.push({
      severity: "blocker",
      code: "handwritten-ui-official-available",
      message:
        "Hand-written UI primitives or variant factories found. Use official HeroUI components first; only write local UI when HeroUI has no official component and document the checked source and exception."
    });
  }

  if (TAILWINDUI_SOURCE_PATTERN.test(code)) {
    issues.push({
      severity: "blocker",
      code: "tailwindui-source-or-catalyst-import",
      message:
        "TailwindUI, Tailwind Plus, or Catalyst source imports found. Treat TailwindUI as reference-only and implement visible UI with official @heroui/react components."
    });
  }

  if (HEADLESS_UI_PATTERN.test(code)) {
    issues.push({
      severity: "blocker",
      code: "headlessui-import",
      message:
        "Headless UI imports found. HeroUI React v3 already provides accessible primitives; use official HeroUI components unless a documented gap exception proves otherwise."
    });
  }

  if (/@nextui-org\/react|@nextui-org\/theme/.test(code)) {
    issues.push({
      severity: "high",
      code: "legacy-nextui-package",
      message: "Legacy @nextui-org package import found. Use @heroui/react for HeroUI v3."
    });
  }

  if (/\bHeroUIProvider\b/.test(code)) {
    issues.push({
      severity: "medium",
      code: "obsolete-provider-assumption",
      message:
        "HeroUIProvider detected. HeroUI React v3 does not require a provider after installation and style import; use I18nProvider only for locale setup."
    });
  }

  if (
    (code.includes("@heroui/react") || css.includes("@import \"tailwindcss\"") || css.includes("@import 'tailwindcss'")) &&
    !/@import\s+["']@heroui\/styles["'];/.test(css)
  ) {
    issues.push({
      severity: "high",
      code: "missing-heroui-styles-import",
      message: "Missing @import \"@heroui/styles\" after @import \"tailwindcss\" in the main CSS file."
    });
  }

  if (/@tailwind\s+(?:base|components|utilities)\s*;/.test(css)) {
    issues.push({
      severity: "high",
      code: "legacy-tailwind-directives",
      message:
        "Tailwind v3 @tailwind directives found. HeroUI React v3 expects Tailwind CSS v4 import style: @import \"tailwindcss\"; then @import \"@heroui/styles\";."
    });
  }

  if (rawColors.length > 0) {
    issues.push({
      severity: "medium",
      code: "raw-tailwind-color",
      message: `Raw Tailwind color utilities found: ${[...new Set(rawColors)].join(", ")}. Prefer HeroUI theme tokens and semantic surfaces.`
    });
  }

  if (/\bspace-[xy]-\d+\b/.test(code)) {
    issues.push({
      severity: "medium",
      code: "unsafe-spacing",
      message: "space-y-* or space-x-* detected. Prefer flex/grid with gap-* for predictable composition."
    });
  }

  for (const [family, count] of paletteFamilies) {
    if (count >= 3) {
      issues.push({
        severity: "medium",
        code: "one-note-palette",
        message: `One-note palette risk: ${family} appears ${count} times. Use HeroUI theme variables and mixed semantic surfaces.`
      });
      break;
    }
  }

  if (!/\b(?:max-w-|min-w-0|w-full|grid-cols-|aspect-|overflow-hidden|container|size-)\b/.test(code)) {
    issues.push({
      severity: "medium",
      code: "responsive-constraint",
      message: "No obvious responsive constraint found. Add stable sizing such as max-w-*, min-w-0, grid tracks, aspect ratio, or overflow guards."
    });
  }

  return {
    status: issues.length === 0 ? "pass" : "needs_revision",
    issues,
    recommendations: [
      "Query the official HeroUI MCP server for selected component docs, props, source styles, and theme variables before implementation.",
      "Use @heroui/react imports and Tailwind v4 CSS import order exactly as documented.",
      "Keep Pencil/Figma approval artifacts linked to the implementation notes.",
      "Verify with desktop and mobile screenshots, interaction checks, accessibility checks, and npx heroui-cli@latest doctor."
    ]
  };
}
