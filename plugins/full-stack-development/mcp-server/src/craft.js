function parseWorkspaceStage(fileMap) {
  const stage = fileMap.get(WORKSPACE_STAGE_FILE);
  if (!stage) {
    return { stageState: null, issue: null };
  }

  try {
    return { stageState: JSON.parse(stage.content), issue: null };
  } catch {
    return {
      stageState: null,
      issue: {
        severity: "blocker",
        code: "invalid-stage-json",
        message: "00-stage.json must be valid JSON."
      }
    };
  }
}

function requirementsApproved(stageState) {
  return Boolean(
    stageState?.approvals?.requirementsApproved === true ||
      ["visual-design-ready", "visual-awaiting-approval", "implementation-ready", "verification"].includes(
        stageState?.currentStage
      )
  );
}

function stageVisualApproved(stageState, visualConfirmation) {
  return Boolean(stageState?.approvals?.visualDesignApproved === true || visualConfirmationApproved(visualConfirmation));
}

function deriveRequirementWorkspaceStage(fileMap, visualConfirmation, stageState) {
  if (!fileMap.has(WORKSPACE_STAGE_FILE)) {
    return "workspace-missing";
  }

  const hasBeforeVisual = WORKSPACE_FILES_BEFORE_VISUAL.every((file) => fileMap.has(file));
  if (!hasBeforeVisual || !requirementsApproved(stageState)) {
    return "requirements-drafting";
  }

  if (!fileMap.has("10-visual-confirmation-plan.md")) {
    return "visual-design-ready";
  }

  if (!stageVisualApproved(stageState, visualConfirmation)) {
    return "visual-awaiting-approval";
  }

  return "implementation-ready";
}

function detectImpactedLayers(changeRequest, explicitLayers = []) {
  const requested = new Set();
  const text = String(changeRequest ?? "");

  for (const layer of explicitLayers) {
    const normalized = String(layer).toLowerCase();
    const match = CHANGE_LAYER_ORDER.find(
      (candidate) => candidate.key === normalized || candidate.stage === normalized
    );
    if (match) {
      requested.add(match.key);
    }
  }

  for (const layer of CHANGE_LAYER_ORDER) {
    if (layer.patterns.some((pattern) => pattern.test(text))) {
      requested.add(layer.key);
    }
  }

  if (requested.size === 0) {
    requested.add("product");
  }

  return CHANGE_LAYER_ORDER.filter((layer) => requested.has(layer.key));
}

function readAgentManifest() {
  const manifestPath = join(OFFICIAL_AGENT_DOC_ROOT, "manifest.json");
  return JSON.parse(readFileSync(manifestPath, "utf8"));
}

function agentDocumentPath(entry) {
  return join(OFFICIAL_AGENT_DOC_ROOT, entry.path ?? entry.relativePath);
}

function publicAgentDocument(docKey, purpose, index, manifest) {
  const entry = manifest.documents[docKey];
  if (!entry) {
    throw new Error(`Missing HeroUI agent document metadata for ${docKey}`);
  }

  return {
    doc: docKey,
    priority: index + 1,
    purpose,
    path: normalizePath(agentDocumentPath(entry)),
    relativePath: entry.path ?? entry.relativePath,
    source: entry.source,
    bytes: entry.bytes,
    sha256: entry.sha256
  };
}

