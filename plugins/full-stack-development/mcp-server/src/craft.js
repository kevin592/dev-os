function asTextList(value) {
  if (Array.isArray(value)) {
    return value.map((item) => String(item).toLowerCase());
  }

  if (!value) {
    return [];
  }

  return String(value)
    .split(/[,;\n]/)
    .map((item) => item.trim().toLowerCase())
    .filter(Boolean);
}

function uniqueByName(components) {
  const seen = new Set();
  const result = [];

  for (const [name, rationale] of components) {
    if (seen.has(name)) {
      continue;
    }

    seen.add(name);
    result.push({ name, rationale });
  }

  return result;
}

function hasAnyNeed(needText, matches) {
  return matches.some((term) => needText.includes(term));
}

function detectPaletteFamilies(code) {
  const matches = code.matchAll(RAW_COLOR_PATTERN);
  const counts = new Map();

  for (const match of matches) {
    const family = match[0].split("-")[1];
    counts.set(family, (counts.get(family) ?? 0) + 1);
  }

  return counts;
}

